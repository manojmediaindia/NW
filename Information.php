                        <h1 class="product_heading">Wi-Fi Information</h1>

                        <div class="Product_img">
                            <img alt="Wi-Fi" src="Images/wifi.gif">
                            <span class="imgs">Un réseau Wi-Fi permet de relier sans fil plusieurs appareils informatiques (ordinateur, routeur, décodeur Internet, etc.) au sein d'un réseau informatique afin de permettre la transmission de données entre eux. Les usages domestiques du Wi-Fi se sont fortement développés ces dernières années à travers les différentes « box » proposées par les fournisseurs d’accès à Internet.</span>
                            <span class="span1">Aujourd’hui, la grande majorité des foyers, des entreprises, des hôtels et des lieux publics tels les aéroports et les gares disposent de réseaux WiFi. En zone urbaine, il est fréquent d’avoir accès à plus d’une vingtaine de réseaux WiFi. Selon la compagnie Jiwire, une ville comme Paris recense en 2011 plus de 3 000 hotspots (réseaux wifi gratuits).</span>
                            <span class="span2">Les ondes de la téléphonie mobile ou du Wi-fi (mais également du sans fil DECT et du Bluetooth) sont différentes des ondes radio et télévision hertziennes car il s'agit de micro-ondes pulsées en extrêmement basses fréquences. Ainsi, le Wi-fi émet sur une porteuse micro-onde de 2,4 Ghz minimum (fréquence d'agitation des molécules d'eau utilisée dans les fours micro-ondes) des fréquences extrêmement basses (texte, voix, image...), “pulsées par saccades” (217 pulses par secondes).</span>
                        </div>
                        <div class="product_studies">
                            <h2 class="product_heading_studies">Ci-dessous, un résumé de quelques risques mentionnés dans de récentes études :</h2>
                            <ul>
                                <li>
                                    &raquo;  Le signal du Wi-fi, même à très faible puissance, désorganise les processus physiologiques et détruit des structures biochimiques. Ce qui entraîne une perte d’étanchéité de la barrière hémato-encéphalique ; une baisse de production de la mélatonine et d'acétylcholine (sommeil, mémoire, concentration…) et des dommages génétiques irréversibles.
                                </li>
                                <li>
                                    &raquo; Il a également été prouvé que l'effet biologique de ces émissions composites est une production de protéines de stress et une résonance avec les fréquences cérébrales (le cerveau émet lui aussi des extrêmement basses fréquences).
                                </li>
                                <li>
                                    &raquo; Il en résulte des pathologies du type insomnies, maux de tête, pertes de concentration et de mémoire, troubles cardiaques et de l'humeur, ceci s'appelle l'électrohypersensibilité (EHS).
                                </li>
                                <li>
                                    &raquo;  Les effets sur le rythme cardiaque ont été démontrés en utilisant un routeur ou un point d'accès WiFi selon une étude du Dr Havas (Université de Trent, Canada)
                                </li>
                                <li>
                                    &raquo; « Un ordinateur portable connecté en WiFi peut endommager l'ADN et baisser la mobilité des spermatozoïdes en seulement quatre heures", a déclaré Conrado Avendano, directeur de recherche au Centre médical de la reproduction Nascentis en Argentine.
                                </li>
